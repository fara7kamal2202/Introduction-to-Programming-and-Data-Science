# Task: Convert temperature from Celsius to Fahrenheit and Kelvin
# Formula: F = (C * 9/5) + 32
# Formula: K = C + 273.15

celsius = 25  # Temperature in Celsius

# Calculate Fahrenheit
fahrenheit = (celsius * 9/5) + 32

# Calculate Kelvin
kelvin = celsius + 273.15

# Print the results in a formatted string
print(f"Temperature: {celsius}°C = {fahrenheit}°F = {kelvin}K")
